function renderSoilGrid() {
  const grid = document.getElementById('soil-grid');
  grid.innerHTML = soils.map(s => {
    const ta = currentLang === 'ta' && soilsTa[s.id];
    const name = ta ? ta.name : s.name;
    const tag = ta ? ta.tag : s.tag;
    const desc = ta ? ta.desc : s.desc;
    return `
      <div class="soil-card" onclick="showSoilDetail('${s.id}')">
        <div class="soil-card-img">
          <img src="soilsense_backend/assests/images/${s.id}.avif"
               alt="${name}"
               style="width:100%;height:100%;object-fit:cover;border-radius:var(--radius) var(--radius) 0 0;"
               onerror="this.style.display='none'">
        </div>
        <div class="soil-card-body">
          <h3>${name}</h3>
          <p>${desc.substring(0, 90)}…</p>
          <div class="soil-tags">
            <span class="soil-tag">${tag}</span>
            <span class="soil-tag">💧 ${s.idealMin}–${s.idealMax}%</span>
          </div>
        </div>
      </div>`;
  }).join('');
}

function showSoilDetail(id) {
  const s = soils.find(x => x.id === id);
  if (!s) return;
  const ta = currentLang === 'ta' && soilsTa[id];
  const t = translations[currentLang];

  document.querySelectorAll('.page').forEach(p => p.classList.remove('active'));
  document.getElementById('soil-detail-page').classList.add('active');

  const heroEl = document.getElementById('detail-hero');
  heroEl.style.backgroundImage = `linear-gradient(rgba(0,0,0,0.4), rgba(0,0,0,0.4)), url('soilsense_backend/assests/images/${s.id}.avif')`;
  heroEl.style.backgroundSize = 'cover';
  heroEl.style.backgroundPosition = 'center';

  const name = ta ? ta.name : s.name;
  const tag = ta ? ta.tag : s.tag;
  const desc = ta ? ta.desc : s.desc;
  const irr = ta ? ta.irr : s.irr;
  const facts = ta ? ta.facts : s.facts;
  const crops = ta ? ta.crops : s.crops;

  document.getElementById('detail-tag').textContent = tag;
  document.getElementById('detail-name').textContent = name;
  document.getElementById('detail-desc').textContent = desc;
  document.getElementById('detail-irr-text').textContent = irr;
  document.getElementById('detail-list').innerHTML = facts.map(f => `<li>${f}</li>`).join('');

  document.getElementById('detail-stats').innerHTML = `
    <div class="detail-stat-card">
      <div class="ds-icon">💧</div>
      <div class="ds-val">${s.idealMin}–${s.idealMax}%</div>
      <div class="ds-lbl">${t.idealMoistureRange}</div>
    </div>
    <div class="detail-stat-card">
      <div class="ds-icon">🌾</div>
      <div class="ds-val">${crops.split(',')[0].trim()}</div>
      <div class="ds-lbl">${currentLang === 'ta' ? 'பொதுவான பயிர்கள்' : 'Common Crops'}</div>
    </div>
    <div class="detail-stat-card">
      <div class="ds-icon">📊</div>
      <div class="ds-val">${s.idealMax - s.idealMin}%</div>
      <div class="ds-lbl">${currentLang === 'ta' ? 'ஈரப்பத சகிப்புத்தன்மை' : 'Moisture Tolerance'}</div>
    </div>`;

  const indicator = document.getElementById('range-indicator');
  indicator.style.left = s.idealMin + '%';
  indicator.style.width = (s.idealMax - s.idealMin) + '%';
  indicator.textContent = s.idealMin + '–' + s.idealMax + '%';

  // Fix labels
  const backBtn = document.querySelector('.btn-back');
  if (backBtn) backBtn.textContent = t.backBtn;

  const moistureH3 = document.querySelector('.moisture-range-visual h3');
  if (moistureH3) moistureH3.textContent = t.idealMoistureRange;

  const aboutH3 = document.getElementById('detail-desc-title');
  if (aboutH3) aboutH3.textContent = t.aboutThisSoil;

  const charH3 = document.querySelectorAll('.info-section h3')[1];
  if (charH3) charH3.textContent = t.keyCharacteristics;

  const irrigH3 = document.querySelector('#detail-irrigation h3');
  if (irrigH3) irrigH3.textContent = t.irrigationGuide;

  window.scrollTo(0, 0);
}